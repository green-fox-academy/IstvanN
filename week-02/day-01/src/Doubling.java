public class Doubling {
  public static void main(String[] args) {

//    Creating the 'baseNum' variable
    int baseNum = 123;

//    Printing the result of 'doubling(baseNum)'
    System.out.println(doubling(baseNum));
  }

  //    This function takes an integer and doubles it
  public static int doubling(int numberToDouble) {
    return numberToDouble * 2;
  }
}
