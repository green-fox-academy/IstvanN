public class TwoNumbers {
  public static void main(String[] args) {

//    Creating the variables

    int a = 13;
    int b = 22;

//    Printing the operations

    System.out.println(a + b);
    System.out.println(b - a);
    System.out.println(a * b);
    System.out.println(((double) b) / a);
    System.out.println(b / a);
    System.out.println(b % a);

  }
}
